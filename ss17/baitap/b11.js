
console.log(Math.abs(-5.5)); // Kết quả: 5.5 (Giá trị tuyệt đối của -5.5 là 5.5)
console.log(Math.abs(10)); // Kết quả: 10 (Giá trị tuyệt đối của 10 là 10)

console.log(Math.ceil(4.4)); // Kết quả: 5 (Làm tròn lên từ 4.4 thành 5)
console.log(Math.ceil(5.8)); // Kết quả: 6 (Làm tròn lên từ 5.8 thành 6)

console.log(Math.floor(5.7)); // Kết quả: 5 (Làm tròn xuống từ 5.7 thành 5)
console.log(Math.floor(6.1)); // Kết quả: 6 (Làm tròn xuống từ 6.1 thành 6)

console.log(Math.round(10.1)); // Kết quả: 10 (10.1 làm tròn xuống thành 10)
console.log(Math.round(20.6)); // Kết quả: 21 (20.6 làm tròn lên thành 21)
